                                        <h2>Jainism</h2>
                                        <img src="web_project/Jain_flag.jpg" width="300" height="200" alt="" class="hboxthumb" />
                                        <p> Jainism,traditionally known as Jain dharma,is an ancient Indian religion belonging to the śramaṇa tradition. The central tenet is non-violence and love towards all living beings. Parasparopagraho Jivanam ("the function of souls is to help one another") is the motto of Jainism. The three main principles of Jainism are ahimsa (non-violence), anekantavada (non-absolutism), and aparigraha (non-possessiveness). Followers of Jainism take five main vows: ahimsa (non-violence), satya (not lying), asteya (not stealing), brahmacharya (chastity), and aparigraha (non-attachment). Jain monks and nuns observe these vows absolutely whereas householders (śrāvakas) observe them within their practical limitations. Self-discipline and asceticism are thus major focuses of Jainism. Notably, Mahatma Gandhi was greatly influenced by Jainism and adopted many Jain principles in his life.</p>
