        <h1 id="Ganesh-Chaturthi"><ins>Ganesh-Chaturthi</ins></h1>    
        <img src="web_project/myphotos/Ganesh-Chaturthi.jpg">
        <p> 
                    Ganesh Chaturthi, another one of important Hindu religious festivals of India, is a       10-day affair of colorful festivities.<br> Huge handcrafted Ganesh idols are             installed in homes or outdoors, in public pandals.<br> Pujas are performed in the  morning and the evening. <br>The last day is the day of Visarjan – immersion of             an idol in a water body.<br> Cultural activities of singing, dancing, and                   theater, and free medical and blood donation camps are held.<br><br>

                    <b>Significance:</b>  It’s the birthday of Lord Ganesha, the elephant-headed God.
                    <br><br>
                    <b>Key attractions:</b> The beautifully crafted life size idols of Ganesha, and the immersion ceremony<br><br>

                    <b>When: </b>The  4th day of the first fortnight (Shukla Chaturthi) in the month of Bhadrapada of the Hindu lunisolar calendar, which corresponds to August or September of the Gregorian calendar
                    <br><br>
                    <b>Where: </b>Celebrated in the states of Maharashtra and Andhra Pradesh with fervor and gaiety<br><br>
        </p>
