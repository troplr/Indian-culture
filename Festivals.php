        <h1 id="Navratri"><ins>Navratri</ins></h1>    
        <img src="web_project/myphotos/navtri.jpg">
        <p>     
                Navratri festival is celebrated by all people throughout India in different ways.<br>In Gujarat, it is a nine day celebration of rejuvenating Garba nights and highly energetic Dandiya Raas dances.<br> People are dressed in beautiful, colorful traditional clothes and the environment is very youthful and invigorating.

                <b>Significance: </b>It represents the celebration of the Goddess Amba (Power) in nine different forms.<br><br>

                <b>Key attractions: </b>The 9 days of dance festivities in Gujarat, the exquisite Chaniya Choli’s (traditional skirt & blouse), and the Gujarati cuisine – Sabudana Khichdi, Mandavi Paak, Singoda ki Kheer, and Potato Wafers<br><br>

                <b>When:</b> The first nine days of the month of Ashwin according to the Hindu lunisolar calendar, which correspond to September or October of the Gregorian calendar
                    <br><br>
                <b>Where:</b> Almost all over the country; most vibrant in Gujarat, Maharashtra and the metros<br><br>
        </p>
    </div>
        
