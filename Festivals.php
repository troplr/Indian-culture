         <h1 id="Holi"><ins>Holi</ins></h1>    
         <img src="web_project/myphotos/holi.jpg">
        <p>
            Also known as the festival of colors, holi is one of the famous festivals of India, celebrated with a lot of<br> fervor across the country. On the eve of Holi, people make huge Holika bonfires and sing and <br>dance around it. On the day of Holi, people gather in open areas and apply dry and wet colors of<br> multiple hues to each other, with some carrying water guns and colored water filled balloons.<br><br>

            <strong>Significance:</strong> It signifies the victory of good (Prince Prahlad) over evil (Holika) and the arrival of spring.<br><br>

            <strong>Key attractions: </strong> Holika bonfire, playing with colors, and bhang thandai<br><br>

            <strong>When:</strong> Full moon (Purnima) of the Phalgun month of the Hindu lunisolar calendar, which corresponds to the month of March of the Gregorian calendar<br><br>

            <strong>Where:</strong> Almost all over the country; most vibrant celebrations can be seen in North Indian states<br><br>
         </p>        
    </div>
    <div class="section-3">
            <h1 id="Dusshera"><ins>Dusshera</ins></h1>
            <img src="web_project/myphotos/dussehra.jpg">
        <p>
              Dussehra, also referred to as Vijayadashami, is also among the most famous festivals of India. <br>It is celebrated in different forms countrywide. Ramlila(enactment of scenes from Ramayana) is held everywhere for 10 days.<br> It’s culminated with “Ravan Dahan” – the burning of huge effigies of Ravana, Meghnath and kumbhakaran which is a real spectacle to see.<br><br>
                <b>Significance:</b> It celebrates the death of the demon king Ravana at the hands of Lord Rama.<br><br>
                <b>Key attractions:</b> Hustle bustle of the decorated markets, Ram-leela acts, and the big event of the burning of effigies of Ravana, Meghnad, and Kumbhakaran
                <br><br>
                <b>When:</b> 10th day of the month of Ashwin according to the Hindu lunisolar calendar, which corresponds to September or October of the Gregorian calendar
                <br><br>
                <b>Where:</b> Pan India
                    <br><br>      
        </p>
