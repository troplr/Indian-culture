        <h1 id="Durga Puja"><ins>Durga Puja</ins></h1>    
        <img src="web_project/myphotos/Kolkata-Durga-Puja-2015.jpg">
        <p>      
                    One of the important festivals of India, Durga Puja is                      celebrated with grandeur by Bengalis, throughout the country. <br>The 10 days         of fast, feast, and worship of Goddess Durga are accompanied by cultural songs,             dances, and dramas.<br> Huge and beautiful Durga idols are made and placed in          specially made artistic Pandals(canopies).<br> People dress in traditional wear              and go around the pandal – hopping, praying, and feasting.<br><br>

                        <b>Significance:</b> It commemorates Lord Rama’s invocation of Goddess Durga before going to war with the demon king Ravana.<br><br>

                        <b>Key attractions:</b> Plush pandals, incredibly beautiful ten armed Durga idols, and the puja
                        <br><br>
                        <b>When: </b>10th day of Ashwina shukla paksha according to the Hindu lunisolar calendar, which corresponds to September or October of the Gregorian calendar
                        <br><br>
                        <b>Where: </b>Kolkata and the metros are the best places to be in India during Durga Puja celebrations<br><br>

                    </p>
            </div>

            <div class="section-3">
                <h1 id="Krishna Janmashtami"><ins>Krishna Janmashtami</ins></h1>    
                <img src="web_project/myphotos/Janmashtami.jpg">
                    <p>             Janmashtami is again a beautiful one among the most important               religious festivals of India.<br> Janmashtami celebrations in Mathura and               Vrindavan are very popular. People fast throughout the day and break it with           a special meal after dusk.<br> Visiting temples, praying, dancing, and               singing bhajans (hymns) at midnight is a part of the celebrations of the            birth of Lord Krishna.<br> Often, small children dress up like Lord Krishna         on this day. <br>Images and picturization of Krishna’s life story are               depicted in the “jhaankis” in temples.<br><br>

                        <b>Significance: </b>It is the annual celebration of the birthday of Lord Krishna.
                        <br><br>
                        <b>Key attractions:</b> The Janmashtami puja and festivities in the temples and the jhaankis of Lord Krishna

                        <b>When: </b>The 8th day (Ashtami) of the Krishna Paksha (dark fortnight) of the month of Bhadrapad according to the Hindu lunisolar calendar, which corresponds to August or September of the Gregorian calendar
                        <br><br>
                        <b>Where:</b> Celebrated by the Hindu community all over, but the festivities at Mathura and Vrindavan are very popular<br><br>
        </p>
