        <h1 id="Diwali"><ins>Diwali</ins></h1>    
            <img src="web_project/myphotos/diwali.jpg">
            <p>
                Diwali, the most prominent Hindu festival of India, is celebrated with a lot of pomp and show.<br> During this festival of lights, houses are decorated with clay lamps, candles, and Ashok leaves. <br>People wear new clothes, participate in family puja, burst crackers, and share sweets with friends, families, and neighbors.<br><br>

                <strong> Significance:</strong> The festival marks the return of Lord Rama, along with his wife Sita and brother Lakshmana, after a long exile of 14 years.<br><br>

                <strong>Key attractions:</strong> Homes decorated with fancy lights, candles and clay lamps, bustling shops and markets, and fireworks and crackers<br><br>

                <strong>When:</strong> The darkest new moon night of Kartik month of the Hindu lunisolar calendar, which corresponds to mid-October – mid-November as per the Gregorian Calendar<br><br>

                <strong>Where:</strong> All over the country<br><br>
            </p>
    </div>
    
