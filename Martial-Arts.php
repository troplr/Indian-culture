                <h1 id="6">Lathi</h1>
                <img src="web_project/myphotos/Martial_Art/6.jpg">
                <p>
                    A lathi is basically a six to eight foot long bamboo stick tipped with a metal blunt. The lathi is used by swinging it back and forth like a sword. The metal blunt is an optional part for a lathi. It is the Indian police’s most used crowd control device! When referring to the weapon itself, a lathi could be considered the world’s oldest weapon. Wielding the lathi involves giving quick lethal blows to the opponent and defending opponent attacks by using the lathi as a shield. A lathial needs to be quick and precise and a good lathial must be able to fight with lathis of different lengths and thicknesses.
