                <p>
                    Often referred to as theGrandfather of hand-to-hand fighting, Kung Fu has been practiced for centuries in its land of origin, China. Used by Chinese warriors as form of attack as well as self-defense. Kung Fu is the general term used for Chinese martial arts.  Although there are a variety of styles of Kung Fu, they all share the same root: to strike your enemy with lightning speed and preventable power. Monks are renowned practitioners of Kung Fu, shaping their bodies and minds into lethal tools of war – with or without weapons.
