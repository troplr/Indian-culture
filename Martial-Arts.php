                <p>
                    A traditional stick-martial art form from South India, the word ‘Silam’ (in Tamil) stands for hills and ‘Mambam’ (the Marhat word for bamboo) stands for bamboo from the hills; these sticks are usually found on the hills in that particular region. The salient parts of it are the very well-structured techniques, deeply articulated to each other, a lot of footwork, the fluency of play, and some fighting strategies.
