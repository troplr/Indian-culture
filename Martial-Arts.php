                <h1 id="3">Gatka</h1>
                <img src="web_project/myphotos/Martial_Art/3.jpg">
                <p>
                    Originating from the state of Punjab, Gatka was developed as a self-defense mechanism by the Sikhs. It was a battle technique created by the Sikh warriors during the martial period of the great Sikh Gurus. It is a style of stick fighting between two or more practitioners, with wooden sticks (called Soti) intended to simulate swords. In Gatka, the "Stick" and "Farri" are also used to substitute the sword and shield respectively for practice and safety purposes. Gatka is a unique art form to defend and display fighting skills and exercise self-control, which is the best part of this time-tested martial art.
                    Learn them here:
                    Gatka Federation of India
                    Indian Fight school
                </p>
